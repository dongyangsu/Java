
public class OpenDemo {
	public static void main(String []args){
		int value=100;
		System.out.println(value++);  //100
		
		System.out.println(value--);  //-100,99
		
		System.out.println(++value);  //101
		
		System.out.println(--value);  //100
		
		System.out.println((++value)/8+(value++)*2);  //205,214
		
		value+=30;
		System.out.println(value); //130
		
		
		
		
		
	}

}
